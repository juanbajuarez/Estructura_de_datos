# Ejercicio número 1 de entrada de datos por consola y casting en Python.

numero1,numero2=input("Ingrese el primer número decimal: "),input("Ingrese el segundo número decimal: ")#SOlicitud de dos decimales por consola
#Aplicacaion de casting para convertir las cadenas a enteros
numero1=int(numero1)
numero2=int(numero2)
#Operaciones algebraicas
print(f"La suma de {numero1} + {numero2} es: {numero1+numero2}")
print(f"La resta de {numero1} - {numero2} es: {numero1-numero2}")
print(f"El producto de {numero1} * {numero2} es: {numero1*numero2}")
print(f"La division de {numero1} / {numero2} es: {numero1/numero2}")
